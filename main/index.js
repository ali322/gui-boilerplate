const { app } = require('electron')
const { openWindow } = require('./lib')
require('./ipc')

let win = null

app.on('ready', () => {
  win = openWindow('index/index.html')
})

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (win === null) {
    win = openWindow('index/index.html')
  }
})

